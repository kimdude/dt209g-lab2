    möjligheten att ställa följdfrågor och få det bekräftat om jag förstått rätt eller inte. Utöver det användes Chat GPT också för att felsöka koden. Många gånger hade koden små 
    slarvfel som jag själv hade svårt att hitta, men Chat GPT pekade snabbt ut dem.
</p>
<p>
